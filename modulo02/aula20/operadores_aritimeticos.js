// operadores aritméticos
const num1 = 10;
const num2 = 5; 
let resultado;

resultado = num1 + num2;
console.log('Soma: ', resultado);
console.log(typeof resultado);

resultado = num1 - num2;
console.log('Subtração: ', resultado);
console.log(typeof resultado);

resultado = num1 * num2;
console.log('Multiplicação: ', resultado);
console.log(typeof resultado);

resultado = num1 / num2;
console.log('Divisão: ', resultado);
console.log(typeof resultado);

const valor1 = 5;
const valor2 = 2;
let result = valor1 ** valor2; // 5^2 = 25
console.log(result);
console.log(typeof result); 

console.log(valor1 % valor2); // resto da divisão
console.log(typeof (valor1 % valor2));
